//Creamos funcion encriptar
function encriptar() {
    let texto = document.getElementById("texto").value;
    let tituloMensaje = document.getElementById("titulo-mensaje");
    let parrafo = document.getElementById("parrafo");
    let mono = document.getElementById("mono");
// creamos variables de reemplazos
    let textoCifrado = texto
      .replace(/e/gi, "enter")
      .replace(/i/gi, "imes")
      .replace(/a/gi, "ai")
      .replace(/o/gi, "ober")
      .replace(/u/gi, "ufat");

      //si hay texto, ciframos
    if (texto.length != 0) {
      document.getElementById("texto").value = textoCifrado;
      tituloMensaje.textContent = "Texto encriptado con éxito";
      parrafo.textContent = "";

      //mensaje para cuando no ingresan texto
    } else {
      mono.src = "./img/mono.png";
      tituloMensaje.textContent = "Ningún mensaje fue encontrado";
      parrafo.textContent = "Ingresa el texto que deseas encriptar o desencriptar";
      //Alerta personalizada para cuando no ingresan texto [sacado de https://www.youtube.com/watch?v=VjPW4ZsuAEE]
      swal("Oh No!", "No has ingresado el texto a cifrar");
    }
  }
